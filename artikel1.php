    <h3 class="w3-border-bottom w3-border-light-grey w3-padding-12"><font size="5">PSM ITS GOES TO WEST JAVA 2016</font></h3>
    <p><font size="4">Studi Banding merupakan salah satu program kerja Departemen External Affair Paduan Suara Mahasiswa Institut Teknologi Sepuluh Nopember (PSM ITS) yang merupakan salah satu bentuk kegiatan yang dilaksanakan guna memperkaya pengetahuan PSM ITS seputar kegiatan dalam dunia Paduan Suara lainnya.</font>
    </p>
    <p><font size="4">PSM ITS sendiri sempat kedatangan tamu dari teman-teman Paduan Suara Mahasiswa Fakultas Kesehatan Masyarakat Universitas Diponegoro Semarang (PSM FKM Undip) pada Nopember 2014. Pada kesempatan kali ini, PSM ITS yang berada di wilayah Timur Pulau Jawa ingin mengenal lebih dekat seperti apa kehidupan teman-teman Paduan Suara yang ada di wilayah Barat Pulau Jawa dengan berkunjung ke universitas-universitas tersebut. Setelah sebelumnya PSM ITS sempat berkunjung ke Paduan Suara Mahasiswa Institut Teknologi Bandung (ITB) berbarengan dengan kegiatan FPS ITB XXIV , kali ini PSM ITS berkunjung ke tiga PSM universitas. PSM UI Paragita (Universitas Indonesia), PSM Unpad (Universitas Padjajaran), dan PSM Unpar (Universitas Parahyangan). PSM ITS sendiri merupakan Unit Kegiatan Mahasiswa yang berada di bawah naungan sebuah Institut. Sehingga ingin mengetahui lebih jauh mengenai kehidupan paduan suara mahasiswa di universitas. Lebih jauh mengenai rutinitas latihan, penerimaan anggota baru, prestasi, program kerja, serta kegiatan-kegiatan pendukung lainnya yang dapat mempererat hubungan anggota secara internal dari masing-masing paduan suara mahasiswa tersebut tersebut.</font>
    </p>
